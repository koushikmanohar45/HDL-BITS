module top_module (
    input clk,
    input x,
    output z
); 
    wire x1,x2,x3;
    reg q1,q2,q3;
    assign x1=x^q1;
    assign x2=x&~q2;
    assign x3=x|~q3;
    always@(posedge clk)begin
        q1<=x1;
        q2<=x2;
        q3<=x3;
    end
    assign z=~(q1|q2|q3);
endmodule
