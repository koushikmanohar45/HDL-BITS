/*
Given the finite state machine circuit as shown, assume that the D flip-flops are initially reset to zero before the machine begins.
Build this circuit.
     +-------------------------------------------------------+
     |          _____                                        |
     +-------\ \      \                                      |
             ) ) XOR   >o----+                               |
x----+-------/ /______/      |                               |
     |                       |       _____________           |
     |                       +------| D          Q|----------+
     |                              |             |          |
     |                              |             |          |
     |                   CLK--------|>          ~Q|          | 
     |                              |_____________|          |
     |                                                       |
     |                                                       |
     |                                                       |
     |                                                       |
     |       +-----+                                         |
     +-------|       \                                       |  
     |       |  AND   )------+                               |   +-----------+
     |   +---|       /       |                               |     \           \
     |   |   +-----+         |        _____________          +------\           \
     |   |                   +-------| D          Q|-----------------)  NOR       >⬤------Z
     |   |                           |             |         +------/           /
     |   |                           |             |         |     /           /
     |   |               CLK---------|>          ~Q|----+    |   +-----------+
     |   |                           |_____________|    |    |
     |   |                                              |    |
     |   |                                              |    |
     |   +----------------------------------------------+    |
     |                                                       |
     |       +-------+                                       |
     +------- \        \                                     |
               >  OR    >------+                             |
         +--- /        /       |                             |
