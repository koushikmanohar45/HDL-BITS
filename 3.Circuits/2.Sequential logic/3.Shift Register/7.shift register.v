Implement the following circuit:
                                                                                                                 
                               _____________               ______________            ______________            ____________
                     IN ------| D          Q|-------------|D            Q|----------|D             |----------|D           |---------OUT  
                              |             |             |              |          |              |          |            |  
                              |             |             |              |          |              |          |            | 
CLK --------------+-----------|>            |         +---|>             |     +----|>             |    +-----|>           |  
                  |           |_____________|         |   |______________|     |    |______________|    |     |____________|   
                  |                 ⬤                |           ⬤            |           ⬤           |            ⬤           
                  +-----------------|-----------------+-----------|------------+------------|-----------+            |
                                    |                             |                         |                        |
resetn------------------------------+-----------------------------+-------------------------+------------------------+
*/


module top_module (
    input clk,
    input resetn,   // synchronous reset
    input in,
    output  out);
    reg[3:0] Q;
    always @(posedge clk)begin
        if(!resetn)begin
             Q<=4'd0;
        end
        else 
            Q<={in,Q[3:1]}; 
    end
    assign out=Q[0];

endmodule
