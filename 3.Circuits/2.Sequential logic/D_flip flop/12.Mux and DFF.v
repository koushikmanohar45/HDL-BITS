/*
Consider the n-bit shift register circuit shown below:

          E      L
          |      |
          +------|--------------------------------------• • • • •---------+-----------------------------------------------------+
          |      |                                                        |                                                     |
          |      +---------------------------------------• • • • •--------|------+----------------------------------------------|---------+                                     
          |      |                                                        |      |                                              |         |
 +--------|------|-----------------------------+                          |      |                                              |         |
 |       |\      |                             |                          |      |                                              |         |                 
 +-------|0|    |\                             |                     +----|------|----------------------------+          +------|---------|------------------------+
         | |----|0\                            |                     |   |\      |                            |          |      |         |                        |
 W-------|1|    |  |-----+                     |                     +---|0\    |\                            |          |    |\          |                        |
         |/  +--|1/      |    +-------------+  |                         |  |---|0\                           |          +----|0\       |\                         |       
             |  |/       +----| D          Q|--+-------• • • • •---------|1/    |  |----+   +------------+    |               |  |------|0\        +----------+    |
 R(n-1)------+                |             |  |                         |/  +--|1/     +---|D          Q|----+---------------|1/       |  |-------|D        Q|----+
                              |             |  |                             |  |/          |            |    |               |/    +---|1/        |          |    |
                  +-----------|>            |  |                     R1------+        +-----|>           |    |                     |   |/   +-----|>         |    |
                  |           +-------------+  |                                      |     +------------+    |              R0 ----+        |     |          |    |
                  |                            |                                      |                       |                              |     +----------+    |
clk---------------+----------------------------|---------• • • • •--------------------+-----------------------|------------------------------+                     |
                                               |                                                              |                                                    |
                                             Q(n-1)                                                           Q1                                                   Q0

Write a Verilog module named top_module for one stage of this circuit, including both the flip-flop and multiplexers
*/

module top_module (
    input clk,
    input w, R, E, L,
    output Q
);
    wire x1,x2;
    assign x1=E?w:Q;
    assign x2=L?R:x1;
    
    always@(posedge clk )begin
            Q<=x2;
    end

endmodule
